% --- Executes on button press in pushbutton15.
function pushbutton15_Callback(hObject, eventdata, handles)
%设置ROI
global obj ROI ROI_row
if str2double(get(handles.edit22,'String'))==0
    set(handles.edit27,'String','请先输入钢珠大小')
else
    ROI_row=str2double(get(handles.edit22,'String'))*100+100;
    str1=['ROI设置成功，建议ROI宽大于',num2str(ROI_row)];
    objRes = get(obj, 'VideoResolution');
    ROI_L=floor(str2double(get(handles.edit29,'String')));
    ROI_W=floor(str2double(get(handles.edit30,'String')));
    ROI=[floor((objRes(2)-ROI_W)/2) floor((objRes(2)+ROI_W)/2) ...
        floor((objRes(1)-ROI_L)/2) floor((objRes(1)+ROI_L)/2)];
    obj.ROIPosition=[ROI(3) ROI(1) ROI(4) ROI(2)];
    hImage = image(zeros(ROI_W, ROI_L, 1));
    preview(obj,hImage);
    set(handles.edit27,'String',str1)
end
