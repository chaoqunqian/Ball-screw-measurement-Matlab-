    axes(handles.axes1);
    obj = videoinput('gentl', 1, 'BayerGR8');   %相机固件信息
    src = getselectedsource(obj);  %src为相机
    set(src,'ExposureAuto','Off') %一次自动曝光
    ep=get(handles.edit15,'String');
    src.ExposureTime = str2double(ep)*1000;
    pause(1)
    im=image_shot(1,[],obj,1,ROI);
    bright_=bright(im);  %计算最亮点
    set(handles.edit28,'String',bright_)
    % epTime=get(src,'ExposureTime');
    % set(handles.edit15,'String',round(epTime/1000));  %显示当前曝光时间
    obj.FramesPerTrigger = 1;
    obj.TriggerRepeat=Inf;
    % obj.ReturnedColorSpace='grayscale';
    objRes=get(obj,'ROIPosition');
    
    % nBands = get(obj, 'NumberOfBands');
    
    ROI_L=str2double(get(handles.edit29,'String'));
    ROI_W=str2double(get(handles.edit30,'String'));
    ROI=[(objRes(4)-ROI_W)/2 (objRes(4)+ROI_W)/2 ...
        (objRes(3)-ROI_L)/2 (objRes(3)+ROI_L)/2];
    
    obj.ROIPosition=[ROI(3) ROI(1) ROI(4) ROI(2)]; %更改视野范围
    im=image_shot(1,[],obj,1,ROI);
    hImage = image(zeros(ROI_W, ROI_L, 1));
    % hImage = image(zeros(objRes(2), objRes(1), 3));
    preview(obj,hImage);
    if isempty(dePara) || ~isempty(dePara==0)
        set(handles.edit27,'String','请填完整设计参数，然后设置ROI')
    end
else
    ROI_L=str2double(get(handles.edit29,'String'));
    ROI_W=str2double(get(handles.edit30,'String'));
    hImage = image(zeros(ROI_W, ROI_L, 1));
    preview(obj,hImage);
end
